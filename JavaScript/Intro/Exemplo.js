// Declarando uma variável
var preco = 54.90;

// Multiplicando um número por 0.1 significa 10%
var desconto = preco * 0.1;

// Subtracao do preco com desconto
var precoDesconto = preco - desconto;

// Imprimindo um valor
console.log("Produto custa R$ " + preco);
console.log("Desconto de 10% R$ " + desconto);
console.log("Total a pagar é: R$ " + precoDesconto);
